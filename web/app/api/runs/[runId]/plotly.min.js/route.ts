import { readFile } from "node:fs/promises";

import { getRepoRootFromWebCwd, getRunDir, getRunFilePath, readWebRun } from "@/lib/run-registry";

export const runtime = "nodejs";
export const dynamic = "force-dynamic";

type RouteContext = {
  params: Promise<{ runId: string }>;
};

export async function GET(_request: Request, context: RouteContext): Promise<Response> {
  const { runId } = await context.params;
  const repoRoot = getRepoRootFromWebCwd();

  try {
    const run = await readWebRun(repoRoot, runId);
    const bundlePath = getRunFilePath(getRunDir(repoRoot, runId), plotlyBundlePath(run.visualReportPath));
    const js = await readFile(bundlePath, "utf-8");
    return new Response(js, {
      headers: {
        "cache-control": "public, max-age=31536000, immutable",
        "content-type": "application/javascript; charset=utf-8",
        "x-content-type-options": "nosniff",
      },
    });
  } catch {
    return Response.json({ error: "plotly bundle not found" }, { status: 404 });
  }
}

function plotlyBundlePath(reportPath: string): string {
  const parts = reportPath.split("/");
  parts[parts.length - 1] = "plotly.min.js";
  return parts.join("/");
}
